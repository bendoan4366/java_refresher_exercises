package composition;

public class Dimensions {

    private int width;
    private int height;
    private int depth;

    public Dimensions(int widtth, int height, int depth) {
        this.width = widtth;
        this.height = height;
        this.depth = depth;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getDepth() {
        return depth;
    }
}
